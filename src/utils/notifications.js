import { toast } from "react-toastify";

export function notifyInfo(msg) {
  toast.info(msg, {
    position: "bottom-left",
    autoClose: 7000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
  });
}

